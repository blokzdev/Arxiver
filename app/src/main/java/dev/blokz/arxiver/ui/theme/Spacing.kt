package dev.blokz.arxiver.ui.theme

import androidx.compose.ui.unit.dp

/**
 * Spacing scale. Screen interiors breathe at [lg]; empty/hero moments at
 * [xxl]. Adopted opportunistically as surfaces are touched — semantic
 * spacing beats magic numbers.
 */
object Spacing {
    val xs = 4.dp
    val sm = 8.dp
    val md = 12.dp
    val lg = 16.dp
    val xl = 24.dp
    val xxl = 32.dp
}
